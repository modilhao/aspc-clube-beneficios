// Função para filtrar parceiros
export const filterPartners = (partners, category, region, searchTerm) => {
  return partners.filter(partner => {
    const matchesCategory = category === "Todos" || partner.categoria === category;
    const matchesRegion = region === "Todas" || partner.regiao === region;
    const matchesSearch = searchTerm === "" || 
      partner.empresa.toLowerCase().includes(searchTerm.toLowerCase()) ||
      partner.servicos.toLowerCase().includes(searchTerm.toLowerCase()) ||
      partner.categoria.toLowerCase().includes(searchTerm.toLowerCase());
    
    return matchesCategory && matchesRegion && matchesSearch;
  });
};

// Função para obter estatísticas
export const getPartnerStats = (partners) => {
  const totalPartners = partners.length;
  const categoriesCount = {};
  const regionsCount = {};
  
  partners.forEach(partner => {
    categoriesCount[partner.categoria] = (categoriesCount[partner.categoria] || 0) + 1;
    regionsCount[partner.regiao] = (regionsCount[partner.regiao] || 0) + 1;
  });
  
  return {
    total: totalPartners,
    categories: categoriesCount,
    regions: regionsCount
  };
};